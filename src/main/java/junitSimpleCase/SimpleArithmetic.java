package junitSimpleCase;

public class SimpleArithmetic {
		
	public boolean isLarger(int numLarge1, int numLarge2){
		return numLarge1 > numLarge2;
		}
	
	public boolean isSmaller(int numSmall1, int numSmall2){
		return numSmall1 < numSmall2;
		}
	
	public boolean isEqual(int numEqual1, int numEqual2){
		return numEqual1 == numEqual2;
		}
	
}
